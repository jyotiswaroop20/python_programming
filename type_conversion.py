print("int to float:",b)
#float to int
c=5.7
d=int(c)
print("float to int:",d)
#int to string
e=10
f=str(e)
print("int to string:",f)
#string to int
g="20"
h=int(g)
print("string to int:",h)
#float to string
i=15.5
